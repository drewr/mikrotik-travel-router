use anyhow::{Context, Result};
use std::env;

fn required(key: &str) -> Result<String> {
    env::var(key).with_context(|| format!("Required variable not set: {}", key))
}

fn optional(key: &str, default: &str) -> String {
    env::var(key).unwrap_or_else(|_| default.to_string())
}

fn main() -> Result<()> {
    dotenvy::dotenv().ok();

    let upstream_ssid = required("UPSTREAM_SSID")?;
    let upstream_pass = required("UPSTREAM_WIFI_PASSWORD")?;
    let travel_ssid   = required("TRAVEL_SSID")?;
    let travel_pass   = required("TRAVEL_WIFI_PASSWORD")?;
    let nextdns_id    = required("NEXTDNS_PROFILE_ID")?;
    let device_name   = required("DEVICE_NAME")?;
    let nextdns_name  = optional("NEXTDNS_DEVICE_NAME", &device_name);
    let wg_port       = optional("WG_LISTEN_PORT", "13231");
    let lan_ula       = optional("LAN_ULA_PREFIX", "fd88:1::1/64");
    let private_key   = required("EXIT_PRIVATE_KEY")?;
    let mtu           = optional("EXIT_MTU", "1320");
    let server_pubkey = required("EXIT_SERVER_PUBKEY")?;
    let preshared_key = required("EXIT_PRESHARED_KEY")?;
    let endpoint_ip   = required("EXIT_ENDPOINT_IP")?;
    let endpoint_port = required("EXIT_ENDPOINT_PORT")?;
    let keepalive     = optional("EXIT_KEEPALIVE", "15");
    let tunnel_ipv6   = required("EXIT_TUNNEL_IPV6")?;

    println!("# Generated by generate");
    println!("# Model: hAP ax2 (C52iG-5HaxD2HaxD)");
    println!();
    println!("# --- Identity ---");
    println!("/system identity set name={device_name}");
    println!("/system note set show-at-login=no");
    println!("/system ntp client set enabled=yes");
    println!("/system ntp client servers add address=pool.ntp.org");
    println!();
    println!("# --- Users ---");
    println!("/user add name=root group=full");
    println!("# From your local machine, push your SSH public key:");
    println!("# ssh -4 -o UserKnownHostsFile=/dev/null -o StrictHostKeyChecking=no root@192.168.88.1 \\");
    println!("#   \"/file print file=mykey.txt; file set mykey.txt contents=\\\"$(cat ~/.ssh/id_ed25519.pub)\\\"; /user ssh-keys import public-key-file=mykey.txt\"");
    println!();
    println!("# --- Bridge ---");
    println!("/interface bridge add name=bridge comment=defconf");
    println!();
    println!("# --- WiFi security profiles ---");
    println!("/interface wifi security");
    println!("add name=Upstream passphrase=\"{upstream_pass}\" ft=no");
    println!("add name=Travel   passphrase=\"{travel_pass}\"");
    println!();
    println!("# --- WiFi interfaces ---");
    println!("/interface wifi");
    println!("set [find default-name=wifi1] channel.band=5ghz-ax channel.skip-dfs-channels=all configuration.mode=station-bridge configuration.ssid=\"{upstream_ssid}\" security=Upstream security.ft=no security.ft-over-ds=no disabled=no");
    println!("set [find default-name=wifi2] disabled=yes");
    println!("add name=wifiTravel master-interface=wifi1 configuration.mode=ap configuration.ssid=\"{travel_ssid}\" security=Travel disabled=no");
    println!();
    println!("# --- Bridge ports ---");
    println!("/interface bridge port");
    println!("remove [find interface=wifi1]");
    println!("add bridge=bridge interface=wifiTravel");
    println!();
    println!("# --- Interface lists ---");
    println!("/interface list member");
    println!("add interface=wifi1 list=WAN");
    println!("add interface=exit  list=WAN");
    println!();
    println!("# --- WAN DHCP client ---");
    println!("/ip dhcp-client add interface=wifi1 default-route-tables=main name=wan-dhcp comment=defconf");
    println!();
    println!("# --- Management restrictions ---");
    println!("/ip neighbor discovery-settings set discover-interface-list=LAN");
    println!("/tool mac-server set allowed-interface-list=LAN");
    println!("/tool mac-server mac-winbox set allowed-interface-list=LAN");
    println!();
    println!("# --- CA certificates (required before DoH) ---");
    println!("# Ensure wifi1 is associated with the upstream AP before running these.");
    println!("# /tool fetch url=https://curl.se/ca/cacert.pem");
    println!("# /certificate import file-name=cacert.pem");
    println!();
    println!("# --- DNS ---");
    println!("/ip dns set allow-remote-requests=yes servers=1.1.1.3,1.0.0.3 use-doh-server=\"https://dns.nextdns.io/{nextdns_id}/{nextdns_name}\" verify-doh-cert=yes");
    println!("/ip dns static add name=router.lan address=192.168.88.1 type=A comment=defconf");
    println!("/ip dns static add name=dns.nextdns.io address=45.90.28.0  type=A");
    println!("/ip dns static add name=dns.nextdns.io address=45.90.30.0  type=A");
    println!("/ip dns static add name=dns.nextdns.io address=2a07:a8c0:: type=AAAA");
    println!("/ip dns static add name=dns.nextdns.io address=2a07:a8c1:: type=AAAA");
    println!();
    println!("# --- NAT ---");
    println!("/ip firewall nat add action=masquerade chain=srcnat comment=\"defconf: masquerade\" ipsec-policy=out,none out-interface-list=WAN");
    println!();
    println!("# --- WireGuard (IPv6 exit tunnel) ---");
    println!("/interface wireguard add name=exit private-key=\"{private_key}\" listen-port={wg_port} mtu={mtu}");
    println!("/interface wireguard peers add interface=exit public-key=\"{server_pubkey}\" preshared-key=\"{preshared_key}\" endpoint-address={endpoint_ip} endpoint-port={endpoint_port} allowed-address=::/0 persistent-keepalive={keepalive}s");
    println!();
    println!("# --- IPv6 ---");
    println!("/ipv6 address add address={tunnel_ipv6} interface=exit advertise=no");
    println!("/ipv6 route add dst-address=::/0 gateway=exit");
    println!("/ipv6 address add address={lan_ula} interface=bridge advertise=yes");
    println!("/ipv6 firewall nat add action=masquerade chain=srcnat out-interface=exit comment=\"exit ipv6 masquerade\"");
    println!("/ipv6 nd add interface=bridge ra-lifetime=1800 advertise-dns=yes other-configuration=no");
    println!();
    println!("# --- Allow WireGuard UDP through IPv4 input firewall ---");
    println!("/ip firewall filter add action=accept chain=input comment=\"allow WireGuard exit UDP\" protocol=udp dst-port={wg_port} in-interface-list=WAN place-before=[find comment=\"defconf: drop all not coming from LAN\"]");

    Ok(())
}
