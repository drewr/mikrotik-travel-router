use anyhow::{Context, Result};
use std::{env, fs, process::Command};

const DEFAULT_LAN_SUBNET: &str = "192.168.88.0/24";

struct LanConfig {
    network:    String,
    gateway:    String,
    dhcp_start: String,
    dhcp_end:   String,
}

fn parse_subnet(cidr: &str) -> Result<LanConfig> {
    let (ip, prefix) = cidr.split_once('/')
        .ok_or_else(|| anyhow::anyhow!("LAN_SUBNET must be CIDR notation, e.g. 192.168.88.0/24"))?;
    anyhow::ensure!(prefix == "24", "Only /24 subnets are supported (got /{})", prefix);
    let octs: Vec<u8> = ip.split('.')
        .map(|o| o.parse::<u8>().with_context(|| format!("Invalid octet in LAN_SUBNET: {}", o)))
        .collect::<Result<_>>()?;
    anyhow::ensure!(octs.len() == 4, "LAN_SUBNET must be an IPv4 address");
    let base = format!("{}.{}.{}.", octs[0], octs[1], octs[2]);
    Ok(LanConfig {
        network:    format!("{}0", base),
        gateway:    format!("{}1", base),
        dhcp_start: format!("{}10", base),
        dhcp_end:   format!("{}254", base),
    })
}

fn required(key: &str) -> Result<String> {
    env::var(key).with_context(|| format!("Required variable not set: {}", key))
}

fn optional(key: &str, default: &str) -> String {
    env::var(key).unwrap_or_else(|_| default.to_string())
}

fn main() -> Result<()> {
    dotenvy::dotenv().ok();

    let upstream_ssid = required("UPSTREAM_SSID")?;
    let upstream_pass = required("UPSTREAM_WIFI_PASSWORD")?;
    let travel_ssid   = required("TRAVEL_SSID")?;
    let travel_pass   = required("TRAVEL_WIFI_PASSWORD")?;
    let nextdns_id    = required("NEXTDNS_PROFILE_ID")?;
    let device_name   = required("DEVICE_NAME")?;
    let nextdns_name  = optional("NEXTDNS_DEVICE_NAME", &device_name);
    let wg_port       = optional("WG_LISTEN_PORT", "13231");
    let lan_ula       = optional("LAN_ULA_PREFIX", "fd88:1::1/64");
    let private_key   = required("EXIT_PRIVATE_KEY")?;
    let mtu           = optional("EXIT_MTU", "1320");
    let server_pubkey = required("EXIT_SERVER_PUBKEY")?;
    let preshared_key = required("EXIT_PRESHARED_KEY")?;
    let endpoint_ip   = required("EXIT_ENDPOINT_IP")?;
    let endpoint_port = required("EXIT_ENDPOINT_PORT")?;
    let keepalive     = optional("EXIT_KEEPALIVE", "15");

    let lan_subnet   = optional("LAN_SUBNET", DEFAULT_LAN_SUBNET);
    let lan          = parse_subnet(&lan_subnet)?;

    let ssh_key_file = env::var("ROOT_SSH_PUBLIC_KEY_FILE").ok().filter(|s| !s.is_empty());
    let router_ip    = optional("ROUTER_IP", "192.168.88.1");

    let exit_ipv4 = optional("EXIT_IPV4", "yes") == "yes";
    let exit_ipv6 = optional("EXIT_IPV6", "yes") == "yes";

    if !exit_ipv4 && !exit_ipv6 {
        anyhow::bail!("At least one of EXIT_IPV4 or EXIT_IPV6 must be yes");
    }

    let tunnel_ipv4: Option<String> = if exit_ipv4 {
        Some(required("EXIT_TUNNEL_IPV4")?)
    } else {
        None
    };
    let tunnel_ipv6: Option<String> = if exit_ipv6 {
        Some(required("EXIT_TUNNEL_IPV6")?)
    } else {
        None
    };

    let allowed_address = match (exit_ipv4, exit_ipv6) {
        (true,  true)  => "0.0.0.0/0,::/0",
        (true,  false) => "0.0.0.0/0",
        (false, true)  => "::/0",
        (false, false) => unreachable!(),
    };

    let add_default_route = if exit_ipv4 { "no" } else { "yes" };

    println!("# Generated by generate");
    println!("# Model: hAP ax2 (C52iG-5HaxD2HaxD)");
    println!();
    println!("# --- Identity ---");
    println!("/system identity set name={device_name}");
    println!("/system note set show-at-login=no");
    println!("/system ntp client set enabled=yes");
    println!("/system ntp client servers add address=pool.ntp.org");
    println!();
    println!("# --- Users ---");
    println!("/user add name=root group=full");
    println!();
    println!("# --- Bridge ---");
    println!("/interface bridge add name=bridge comment=defconf");
    println!();
    println!("# --- WiFi security profiles ---");
    println!("/interface wifi security");
    println!("add name=Upstream passphrase=\"{upstream_pass}\" ft=no");
    println!("add name=Travel   passphrase=\"{travel_pass}\"");
    println!();
    println!("# --- WiFi interfaces ---");
    println!("/interface wifi");
    println!("set [find default-name=wifi1] channel.band=5ghz-ax channel.skip-dfs-channels=all configuration.mode=station-bridge configuration.ssid=\"{upstream_ssid}\" security=Upstream security.ft=no security.ft-over-ds=no disabled=no");
    println!("set [find default-name=wifi2] disabled=yes");
    println!("add name=wifiTravel master-interface=wifi1 configuration.mode=ap configuration.ssid=\"{travel_ssid}\" security=Travel disabled=no");
    println!();
    println!("# --- Bridge ports ---");
    println!("/interface bridge port");
    println!("remove [find interface=wifi1]");
    println!("add bridge=bridge interface=wifiTravel");
    println!();
    println!("# --- Interface lists ---");
    println!("/interface list member");
    println!("add interface=wifi1 list=WAN");
    println!("add interface=exit  list=WAN");
    println!();
    println!("# --- WAN DHCP client ---");
    println!("/ip dhcp-client add interface=wifi1 add-default-route={add_default_route} name=wan-dhcp comment=defconf");
    println!();
    println!("# --- Management restrictions ---");
    println!("/ip neighbor discovery-settings set discover-interface-list=LAN");
    println!("/tool mac-server set allowed-interface-list=LAN");
    println!("/tool mac-server mac-winbox set allowed-interface-list=LAN");
    println!();
    println!("# --- CA certificates (required before DoH) ---");
    println!("# Ensure wifi1 is associated with the upstream AP before running these.");
    println!("# /tool fetch url=https://curl.se/ca/cacert.pem");
    println!("# /certificate import file-name=cacert.pem");
    println!();
    println!("# --- DNS ---");
    println!("/ip dns set allow-remote-requests=yes servers=1.1.1.3,1.0.0.3 use-doh-server=\"https://dns.nextdns.io/{nextdns_id}/{nextdns_name}\" verify-doh-cert=yes");
    println!("/ip dns static add name=router.lan address={} type=A comment=defconf", lan.gateway);
    println!("/ip dns static add name=dns.nextdns.io address=45.90.28.0  type=A");
    println!("/ip dns static add name=dns.nextdns.io address=45.90.30.0  type=A");
    println!("/ip dns static add name=dns.nextdns.io address=2a07:a8c0:: type=AAAA");
    println!("/ip dns static add name=dns.nextdns.io address=2a07:a8c1:: type=AAAA");
    println!();
    println!("# --- NAT ---");
    println!("/ip firewall nat add action=masquerade chain=srcnat comment=\"defconf: masquerade\" ipsec-policy=out,none out-interface-list=WAN");
    println!();
    println!("# --- WireGuard (exit tunnel) ---");
    println!("/interface wireguard add name=exit private-key=\"{private_key}\" listen-port={wg_port} mtu={mtu}");
    println!("/interface wireguard peers add interface=exit public-key=\"{server_pubkey}\" preshared-key=\"{preshared_key}\" endpoint-address={endpoint_ip} endpoint-port={endpoint_port} allowed-address={allowed_address} persistent-keepalive={keepalive}s");

    if exit_ipv4 {
        let ipv4 = tunnel_ipv4.as_deref().unwrap();
        println!();
        println!("# --- IPv4 exit routing ---");
        println!("/ip address add address={ipv4} interface=exit");
        println!("/ip route add dst-address=0.0.0.0/0 gateway=exit");
        println!("# Endpoint host route — reinstalled dynamically on each DHCP lease");
        println!("/system script add name=exit-endpoint-update source={{");
        println!("    :local gw [/ip dhcp-client get [find name=wan-dhcp] gateway]");
        println!("    :if ($gw != \"\") do={{");
        println!("        :do {{ /ip route remove [find comment=exit-endpoint] }} on-error={{}}");
        println!("        /ip route add dst-address={endpoint_ip}/32 gateway=$gw comment=exit-endpoint");
        println!("    }}");
        println!("}}");
        println!("/ip dhcp-client set [find name=wan-dhcp] script=exit-endpoint-update");
    }

    if exit_ipv6 {
        let ipv6 = tunnel_ipv6.as_deref().unwrap();
        println!();
        println!("# --- IPv6 exit routing ---");
        println!("/ipv6 address add address={ipv6} interface=exit advertise=no");
        println!("/ipv6 route add dst-address=::/0 gateway=exit");
        println!("/ipv6 address add address={lan_ula} interface=bridge advertise=yes");
        println!("/ipv6 firewall nat add action=masquerade chain=srcnat out-interface=exit comment=\"exit ipv6 masquerade\"");
        println!("/ipv6 nd add interface=bridge ra-lifetime=1800 advertise-dns=yes other-configuration=no");
    }

    println!();
    println!("# --- Allow WireGuard UDP through IPv4 input firewall ---");
    println!("/ip firewall filter add action=accept chain=input comment=\"allow WireGuard exit UDP\" protocol=udp dst-port={wg_port} in-interface-list=WAN place-before=[find comment=\"defconf: drop all not coming from LAN\"]");

    if lan_subnet != DEFAULT_LAN_SUBNET {
        println!();
        println!("# --- LAN subnet ({lan_subnet}) ---");
        println!("# These run last because changing the bridge IP drops any active SSH session.");
        println!("# If importing via SSH, the connection will drop here but /import continues");
        println!("# on the router. Reconnect at {}/24 when done.", lan.gateway);
        println!("/ip address set [find interface=bridge] address={}/24 network={}", lan.gateway, lan.network);
        println!("/ip pool set [find name=default-dhcp] ranges={}-{}", lan.dhcp_start, lan.dhcp_end);
        println!("/ip dhcp-server network set [find address=192.168.88.0/24] address={}/24 gateway={} dns-server={}", lan.network, lan.gateway, lan.gateway);
        println!("/ip dns static set [find name=router.lan] address={}", lan.gateway);
    }

    if let Some(ref key_path) = ssh_key_file {
        let key = fs::read_to_string(key_path)
            .with_context(|| format!("Failed to read ROOT_SSH_PUBLIC_KEY_FILE: {}", key_path))?;
        let key = key.trim();
        let remote_cmd = format!(
            "/file print file=mykey.txt; file set mykey.txt contents=\"{key}\"; /user ssh-keys import public-key-file=mykey.txt"
        );
        eprintln!("Installing SSH public key on root@{router_ip}...");
        let status = Command::new("ssh")
            .args([
                "-4",
                "-o", "UserKnownHostsFile=/dev/null",
                "-o", "StrictHostKeyChecking=no",
                &format!("root@{router_ip}"),
                &remote_cmd,
            ])
            .status()
            .context("Failed to run ssh")?;
        if !status.success() {
            anyhow::bail!("SSH key installation failed — is the RSC applied and root user created?");
        }
        eprintln!("SSH key installed.");
    }

    Ok(())
}
